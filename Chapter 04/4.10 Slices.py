# 4.10 Slices
foods = ['chotpoti', 'fuchka', 'jhalmuri', 'chira vaja', 'cha', 'beguni', 'piazu', 'chola', 'ice cream']

print(len(foods))
print("The first three items in the list are:")
print(foods[:3])

print("\nThree items from the middle of the list are:")
print(foods[3:6])

print("\nThe last three items from the list are:")
print(foods[-3:])